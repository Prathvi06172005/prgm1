package javaclass;
public class Prg13 {
    public static void main(String[] args) {
        int i=8;
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(--i);
        System.out.println(--i);
        System.out.println(i++);
        System.out.println(i);
        int a=12;
        int b=a++ + a++;
        System.out.println(b);
            
    }
    
}
